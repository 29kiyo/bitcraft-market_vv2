// ひらがな→カタカナ変換
function toKatakana(str) {
  return str.replace(/[\u3041-\u3096]/g, c => String.fromCharCode(c.charCodeAt(0) + 0x60));
}
// カタカナ→ひらがな変換
function toHiragana(str) {
  return str.replace(/[\u30A1-\u30F6]/g, c => String.fromCharCode(c.charCodeAt(0) - 0x60));
}

// 読み仮名マッチ用：ひらがな入力 → 漢字キーを検索してenを返す
function searchByYomi(q) {
  const qHira = toHiragana(q); // カタカナもひらがなに統一
  const matched = new Set();
  for (const [kanji, yomi] of Object.entries(ITEM_YOMI)) {
    if (yomi.includes(qHira) || qHira.includes(yomi)) {
      // その漢字キーに対応する英語を探す
      for (const [ja, en] of Object.entries(ITEM_TRANSLATIONS)) {
        if (ja.includes(kanji)) matched.add(en.toLowerCase());
      }
    }
  }
  return matched;
}

// 英語→日本語（逆引き用）
const ITEM_TRANSLATIONS_EN_JA = {};
for (const [ja, en] of Object.entries(ITEM_TRANSLATIONS)) {
  if (!ITEM_TRANSLATIONS_EN_JA[en]) {
    ITEM_TRANSLATIONS_EN_JA[en] = ja;
  }
}

function translateQuery(query) {
  const q = query.trim();
  if (!q) return q;

  // 日本語が含まれているか判定
  const hasJapanese = /[\u3040-\u30ff\u4e00-\u9faf]/.test(q);
  if (!hasJapanese) return q; // 英語ならそのまま

  // 完全一致
  if (ITEM_TRANSLATIONS[q]) return ITEM_TRANSLATIONS[q];

  // 部分一致（長いキーを優先）
  const sorted = Object.entries(ITEM_TRANSLATIONS).sort((a, b) => b[0].length - a[0].length);
  for (const [ja, en] of sorted) {
    if (q.includes(ja)) return en;
  }

  // 逆方向：入力がキーに含まれる場合（「パイ」→「ピュレライト」など）
  for (const [ja, en] of sorted) {
    if (ja.includes(q)) return en;
  }

  return q;
}

